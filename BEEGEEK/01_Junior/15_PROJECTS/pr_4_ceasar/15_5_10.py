n = input()
# –∏—â–µ–º –¥–ª–∏–Ω—É —Å–ª–æ–≤ –ø–µ—Ä–µ–≤–æ–¥–∏–º –≤ —Å–ª–æ–≤–∞—Ä—å –≤ –≤–∏–¥–µ —á–∏—Å–µ–ª
s = n
for j in n:
    if j in '*,.!@"-':
        s = s.replace(j, '')
g = [len(i) for i in s.split()]

# –æ–±—ä—è–≤–ª—è–µ–º –ø–µ—Ä–µ–º–µ–Ω–Ω—É—é —Å—á–µ—Ç—á–∏–∫, –∫–æ–≥–¥–∞ –ø–æ–ø–∞–¥–∞–µ—Ç—Å—è –ø—Ä–æ–±–µ–ª –ø–µ—Ä–µ—Ö–æ–¥–∏–º –Ω–∞ —Å–ª–µ–¥ —è—á–µ–π–∫—É –≤ —Å–ª–æ–≤–∞—Ä–µ
count = 0
word_new = ''
for d in n:
    number = ord(d)
    if d == ' ':
        count += 1
        word_new += chr(number)
    elif 65 <= number <= 90:
        number += g[count]
        if number > 90:
            number = number - 26
            word_new += chr(number)
        else:
            word_new += chr(number)
    elif 97 <= number <= 122:
        number += g[count]
        if number > 122:
            number = number - 26
            word_new += chr(number)
        else:
            word_new += chr(number)
    else:
        word_new += chr(number)

print(word_new)
