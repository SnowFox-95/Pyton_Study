print('–°–æ–µ–¥–∏–Ω–µ–Ω–∏–µ —Å–ø–∏—Å–∫–æ–≤ –∏ —Å–æ—Ä—Ç–∏—Ä–æ–≤–∫–∞ –≤—ã–±–æ—Ä–æ–º', merge_sel(numbers1, numbers2))
print('–°–æ–µ–¥–∏–Ω–µ–Ω–∏–µ —Å–ø–∏—Å–∫–æ–≤ –∏ —Å–æ—Ä—Ç–∏—Ä–æ–≤–∫–∞ –º–µ—Ç–æ–¥–æ–º sort()', merge_sort(numbers1, numbers2))
