# –æ–±—ä—è–≤–ª–µ–Ω–∏–µ —Ñ—É–Ω–∫—Ü–∏–∏ c —Å–æ—Ä—Ç–∏—Ä–æ–≤–∫–æ–π –ø—É–∑—ã—Ä—å–∫–æ–º
def merge_puz(list1, list2):
    list3 = list1 + list2
    for i in range(len(list3) - 1):
        for j in range(len(list3) - i - 1):
            if list3[j] > list3[j + 1]:
                list3[j], list3[j + 1] = list3[j + 1], list3[j]
    return list3


# –æ–±—ä—è–≤–ª–µ–Ω–∏–µ —Ñ—É–Ω–∫—Ü–∏–∏ c —Å–æ—Ä—Ç–∏—Ä–æ–≤–∫–æ–π –≤—ã–±–æ—Ä–æ–º
def merge_sel(list1, list2):
    list3 = list1 + list2
    for i in range(len(list3) - 1):
        for j in range(i + 1, len(list3)):
            if list3[j] < list3[i]:
                list3[j], list3[i] = list3[i], list3[j]
    return list3


# –æ–±—ä—è–≤–ª–µ–Ω–∏–µ —Ñ—É–Ω–∫—Ü–∏–∏ c –º–µ—Ç–æ–¥–æ–º sort()
def merge_sort(list1, list2):
    list3 = list1 + list2
    list3.sort()
    return list3


