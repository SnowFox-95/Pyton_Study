"""
Переворот числа
Дано пятизначное или шестизначное натуральное число. Напишите программу, которая
 изменит порядок его последних пяти цифр на обратный.

Формат входных данных
На вход программе подается одно натуральное пятизначное или шестизначное число.

Формат выходных данных
Программа должна вывести число, которое получится в результате разворота, указанного
  в условии задачи. Число нужно выводить без незначащих нулей.
"""
numb = input()
if len(numb) % 2 == 0:
    print((numb[:1] + numb[::-1])[:-1])
else:
    print((int(str(numb[::-1]))))
